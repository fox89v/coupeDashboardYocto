  fi

  echo "üõ†Ô∏è  Building image for $MACHINE..."
  TARGET="sa-image-minimal"
  if [ "$USE_CPULIMIT" = true ]; then
    nice -n 10 cpulimit -l "$CPU_PERCENT" -- bitbake "$TARGET"
  else
    nice -n 10 bitbake "$TARGET"
  fi
  echo "‚úÖ Image built in $IMG_PATH/"
  exit 0
